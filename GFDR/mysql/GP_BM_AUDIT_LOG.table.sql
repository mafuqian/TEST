CREATE TABLE GP_BM_AUDIT_LOG(
    DATA_ID VARCHAR(64) NOT NULL   COMMENT '数据ID' ,
    FUNC_ID VARCHAR(20) NOT NULL   COMMENT '菜单号' ,
    FUNC_NAME VARCHAR(60)    COMMENT '菜单名' ,
    ORG_ID VARCHAR(14)    COMMENT '机构号' ,
    DEPART_ID VARCHAR(14)    COMMENT '部门号' ,
    WORK_DATE VARCHAR(8)    COMMENT '工作日期' ,
    TLRNO VARCHAR(20)    COMMENT '操作员号' ,
    OP_TYPE VARCHAR(10)    COMMENT '操作类型' ,
    VALUE_OBJECT_NAME VARCHAR(128)    COMMENT '数据对象名' ,
    VALUE_BEFORE TEXT    COMMENT '修改前的值' ,
    VALUE_AFTER TEXT    COMMENT '修改后的值' ,
    VALUE_META TEXT    COMMENT '操作数据元数据' ,
    OP_TIME VARCHAR(20)    COMMENT '修改时间' ,
    IP VARCHAR(512)    COMMENT '操作员IP' ,
    RSV1 VARCHAR(180)    COMMENT '备用1' ,
    RSV2 VARCHAR(180)    COMMENT '备用2' ,
    RSV3 VARCHAR(180)    COMMENT '备用3' ,
    RSV4 VARCHAR(180)    COMMENT '备用4' ,
    RSV5 VARCHAR(180)    COMMENT '备用5' ,
    REF_ID VARCHAR(1000)    COMMENT '' ,
    PRIMARY KEY (DATA_ID)
) COMMENT = '审计日志表' ENGINE=InnoDB;
